            <center><h4><b>Komputer Akuntansi ( D3 )</b></h4></center>

              <h5><b>1.1. Pendahuluan</b></h5>
              <p>Program studi Komputerisasi Akuntansi merupakan program studi yang berusia relatif muda dan dikembangkan berdasarkan kebutuhan tenaga kerja pada industri yang menginginkan adanya tenaga kerja yang mempunyai skill di bidang komputer serta memahami akuntansi sebagai bahasa bisnis.</p>
              <p>Saat ini, tenaga kerja yang memenuhi persyaratan skill di bidang tersebut masih terasa sangat kurang dan tenaga kerja dengan skill akuntansi dan komputer akan selalu diperlukan selama masih terdapat perusahaan-perusahaan dan organisasi-organisasi lain yang memerlukan akuntansi berkomputer dalam operasinya.</p>
              <p>Perkembangan industri saat ini telah memicu perkembangan ke enterprise information system, yaitu sistem informasi yang akan diterapkan pada ruang lingkup yang besar dari perusahaan. Besar disini meliputi jangkauan transaksi serta area yang luas meliputi seluruh dunia. Hal tersebut dimungkinkan karena perkembangan teknologi Intranet, Internet, serta Extranet yang sangat pesat. EIS mempunyai inti pada dua bidang, yaitu serta pengetahuan bisnis. Hal tersebut merupakan inti dari program studi Komputerisasi Akuntansi.</p>
              <p>Dalam program studi ini, mahasiswa dididik untuk mempunyai pola pikir sebagai pengembang sistem informasi akuntansi. Dengan pola pikir tersebut, mahasiswa akan diuntungkan karena selain mampu mengembangkan sistem informasi akuntansi sendiri, mahasiswa juga mengetahui bagaimana seharusnya suatu software akuntansi bekerja. Dengan demikian, lulusan dari program studi Komputerisasi Akuntansi tidak akan mengalami kesulitan menggunakan berbagai software akuntansi.</p>
              <p>Dalam kaitan sebagai pengembang sistem informasi akuntansi, mahasiswa akan dididik secara konsisten untuk menggunakan piranti dan metodologi yang berbasis Object-Oriented karena metodologi serta piranti tersebut yang banyak digunakan oleh industri pada saat ini dan masa yang akan datang. Metodologi seperti RUP, Extreme Programming, serta metodologi-metodologi Agile lainnya seharusnya dipahami dengan baik oleh mahasiswa karena merupakan materi yang diajarkan di Komputerisasi Akuntansi. Demikian juga dengan piranti pemodelan berbasis UML merupakan kompetensi dari mahasiswa Komputerisasi Akuntansi. Seiring dengan makin meningkatkan penggunaan software open source di masa kini dan masa yang akan datang (komitmen dari Oracle, IBM, serta HP menunjukkan hal tersebut), program studi Komputerisasi Akuntansi mendeklarasikan diri sebagai program studi yang memfokuskan diri pada peranti berbasis open source mulai dari awal kuliah sampai mahasiswa lulus.</p>
              <p>Sementara itu, dalam kaitannya dengan sistem informasi akuntansi, mahasiswa akan mendapatkan materi yang memadai dalam hal akuntansi serta sistem akuntansi. Akuntansi manual mulai dari akuntansi pengantar, akuntansi keuangan menengah dan lanjut, akuntansi kos dan akuntansi manajemen akan memberi bekal yang memadai bagi lulusan Komputerisasi Akuntansi. Berbagai materi pendukung lainnya untuk kepentingan pengembangan diri (seperti etika bisnis dan profesi, aspek legal, dan lain-lain) dan pengkayaan wawasan bisnis juga diharapkan akan memperkuat lulusan dari Komputerisasi Akuntansi.</p>

              <h5><b>1.2. Sekilas Program Studi Komputerisasi Akuntansi</b></h5>
              <p>Program studi Komputerisasi Akuntansi di STMIK AKAKOM didirikan pada tahun 1999. Pada saat berdiri, program studi Komputerisasi Akuntansi memperoleh status Terdaftar berdasarkan SK Dirjen Dikti Nomor 39/DIKTI/Kep/1999. Setelah itu program studi Komputerisasi Akuntansi terus menerus berusaha memperbaiki diri dan meningkatkan kualitas dan statusnya.</p>
              <p>Program studi Komputerisasi Akuntansi mendapatkan Akreditasi BAN-PT dengan nilai B pada tahun 2012. Perolehan status tersebut merupakan bukti kemauan dari pihak akademis untuk selalu memberikan peningkatan kualitas pendidikan.</p>

              <h5><b>1.3. Kompetensi</b></h5>
              <h6><b>1.3.1. Kompetensi Umum</b></h6>
              <ol>
                <li>Mampu mengimplementasikan TI secara kreatif dan inovatif di masyarakat</li>
                <li>Mampu mengembangkan diri untuk peningkatan wawasan dan keahlian</li>
                <li>Mampu beradaptasi dan bekerjasama dalam team work</li>
                <li>Mampu berkomunikasi aktif dalam bahasa internasional</li>
                <li>Mampu menjunjung tinggi etika dan profesionalisme</li>
              </ol>

              <h6><b>1.3.2. Kompetensi Khusus</b></h6>
              <ol>
                <li>Mampu membangun aplikasi akuntansi berbasis web.</li>
                <li>Menguasai akuntansi bidang pajak</li>
                <li>menguasai tool akuntansi</li>
              </ol>

              <h6><b>1.3.3. Gradasi Kompetensi</b></h6>
              <ol>
                <li>Tahun ke-1 : "Mampu Menyusun Laporan keuangan Perusahaan Berbasis IT"</li>
                <li>Tahun ke-2 :"Mampu menyusun dan Melakukan penghitungan Pajak berbasis IT</li>
                <li>Tahun ke-3:"Mampu Menyusun sistem akuntansi berbasis IT"</li>
              </ol>

              <h6><b>1.3.4. Usaha</b></h6>
              <p>Untuk mencapai kompetensi tersebut, program studi Komputerisasi Akuntansi melakukan berbagai aktivitas belajar mengajar serta aktivitas-aktivitas lain untuk menambah nilai. Diantaranya adalah sebagai berikut:</p>
              <ol>
                <li>Workshop peranti pengembangan serta aplikasi berbasis open source</li>
                <li>Kuliah umum berkaitan dengan kompetensi utama tersebut.</li>
                <li>Kunjungan ke industri</li>
                <li>Evaluasi kurikulum secara periodik</li>
                <li>Peningkatan kualitas dosen (pendidikan lanjut maupun mengikuti berbagai kegiatan yang berhubungan dengan kompetensi utama jurusan)</li>
                <li>Memfasilitasi kelompok belajar (Python, Linux. GNUCash, Akuntansi)</li>
                <li>Meningkatkan skill dalam berbahasa Inggris, TOEFL menjadi syarat kelulusan</li>
                <li>Evaluasi kinerja dosen.</li>
              </ol>

              <h5><b>1.4. Peluang Karir</b></h5>
              <p>Hasil pengamatan dan survey pada berbagai lapangan kerja yang tersedia serta lulusan Komputerisasi Akuntansi yang telah diserap di dunia kerja menunjukkan bahwa lulusan program studi ini mempunyai prospek yang sangat cerah. Beberapa peluang karir dari lulusan ini antara lain:</p>
              <ol>
                <li>Pengembang sistem informasi, khususnya sistem informasi akuntansi</li>
                <li>Accounting software maintenance</li>
                <li>Analis sistem</li>
                <li>Pengembang web e-commerce</li>
                <li>Auditor sistem informasi</li>
                <li>Pekerja dalam sistem akuntansi</li>
              </ol>

              <h5><b>1.5. Syarat Kelulusan</b></h5>
              <p>Untuk menyelesaikan pendidikan pada program studi Komputerisasi Akuntansi - D3, mahasiswa diwajibkan telah mengumpulkan sekurang-kurangnya 110 sks, dengan mata kuliah wajib sejumlah 80 sks, dan sisanya merupakan mata kuliah pilihan. Persyaratan mata kuliah wajib sebesar 80 sks ini dapat dipenuhi dengan mengambil semua mata kuliah wajib. Semua mata kuliah wajib dan praktikum nilai minimum adalah C (nilai D berarti tidak lulus).</p>
              <p>1. Tugas Akhir .Tugas akhir mahasiswa, sesuai dengan misi serta visi yang telah ditetapkan di atas, berupa pembuatan software dalam bidang akuntansi. Meskipun pemilihan peranti untuk pembuatan software tersebut bebas, mahasiswa dianjurkan menggunakan peranti berbasis teknologi obyek dan merupakan free software atau open source development tools. Tugas akhir mempunyai bobot 4 SKS dan harus diambil oleh setiap mahasiswa.</p>
