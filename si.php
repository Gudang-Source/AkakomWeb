            <center><h4><b>Sistem Informasi ( S1 )</b></h4></center>

              <h5><b>1.1.   Pendahuluan</b></h5>
              <p>Dewasa ini, orang disibukkan dengan banyaknya teknologi baru yang muncul di sekitarnya. Kadang, bahkan kita tidak tahu apa sebenarnya yang benar-benar kita perlukan. Orang berlomba-lomba membeli peralatan teknologi semisal handphone tercanggih, laptop terbaru dan yang lainnya. Sedangkan perusahaan berlomba-lomba membeli peralatan kantor yang dianggap serba terbaru, tercanggih, terdepan. Dalam hal ini, orang menjadi lupa dengan manfaat sesungguhnya dari peralatan-peralatan tersebut. Untuk itu, kini teknologi informasi menjadi sangat penting. Orang, perusahaan berusaha untuk secepatnya memperoleh informasi. Di era informasi yang begitu cepat tersebut, kita membutuhkan tools yang baik yang dapat digunakan untuk membantu mempercepat informasi sampai di tangan. Demikian juga dengan sarana komunikasi yang lainnya, dengan berkembangnya telpon genggam kita bisa berkomunikasi dengan relasi kita kapanpun dan dimanapun. Televisi juga tidak kalah penting, kita dapat melihat siaran televisi dari mancanegara dengan bantuan parabola. Dan kini internet sudah menjadi hal yang biasa di masyarakat kita, terutama masyarakat perkotaan. Warnet tumbuh bak jamur di musim hujan. Dengan adanya internet, kita bisa mengetahui informasi dari mana saja di seluruh dunia. Dengan demikian kita bisa mengembangkan diri, perusahaan kita berbekal informasi baik yang kita peroleh.</p>
              <p>Untuk itu, teknologi informasi sangat penting. Kita bisa menentukan teknologi mana yang akan kita gunakan. Juga kita bisa memilah informasi berdasarkan kebutuhan. Teknologi informasi telah merambah secara luas dalam berbagai bidang kehidupan seperti manajemen, teknik, kedokteran, psikologi, pendidikan hingga seni. Untuk itu, kebutuhan akan pakar bidang teknologi informasi dipastikan semakin meningkat setiap tahunnya. Apalagi ilmu dalam bidang ini sangat dinamis, hampir setiap hari dihasilkan teknologi baru.</p>
							<p>Program Studi Sistem Informasi merupakan salah satu cabang dari pohon ilmu Informatika dan Komputer. Program ini dikhususkan untuk menggarap sistem informasi dari berbagai sisi. Hal ini meliputi bagaimana data dapat disimpan dengan baik, bagaimana  informasi dapat diperoleh dengan baik, dan bagaimana supaya informasi yang dihasilkan dapat memberikan manfaat bagi yang menggunakannya, baik perusahaan, maupun perorangan.</p>

              <h5><b>1.2.   Sekilas Program Studi Sistem Informasi</b></h5>
              <p>Program Studi Sistem Informasi merupakan salah satu program studi yang diselenggarakan oleh STMIK AKAKOM. Program Studi Sistem Informasi mulai diselenggarakan berdasarkan SK No. 300/Dikti/Kep/1992 saat itu dengan nama Manajemen Informatika. Dan saat ini telah terakreditasi ‘B’ berdasarkan SK No. 011/BAN-PT/Ak-X/S1/VIII/2006.</p>
              <p>Pada tahun 2009  Program Studi ini telah menerapkan kurikulum berbasis kompetensi. Dalam kurikulum yang dirancang untuk 8 semester ini, program studi mengarahkan minat sistem informasi berorientasi bisnis dan akuntansi. Dengan demikian, diharapkan lulusan akan dapat menganalisis, merancang, mengimplementasikan sistem informasi yang bisa membantu keputusan bisnis ataupun akuntansinya. Dengan kurikulum yang dirancang seperti itu, maka peminat program studi ini, bisa berasal dari berbagai jurusan yang ada di SLTA.</p>
							<p>Mahasiswa program studi ini, berasal dari berbagai daerah, yang menunjukkan bahwa program studi ini sudah dikenal di daerah mereka. Namun, tidak bisa dipungkiri bahwa program studi harus terus meningkatkan mutu agar selalu bisa bersaing.</p>

              <h5><b>1.3.   Kompetensi Umum</b></h5>
              <ol>
                <li>Mampu mengimplementasikan TI secara kreatif dan inovatif di masyarakat</li>
                <li>Mampu mengembangkan diri untuk peningkatan wawasan dan keahlian</li>
                <li>Mampu beradaptasi dan bekerjasama dalam team work</li>
                <li>Mampu berkomunikasi aktif dalam bahasa internasional</li>
                <li>Mampu menjunjung tinggi etika dan profesionalisme</li>
              </ol>

              <h6><b>1.3.1.  Kompentensi Khusus</b></h6>
              <p>Mampu menganalisis, membangun dan mengembangkan  SI proses bisnis untuk operasional dan manajerial</p>

              <h6><b>1.3.2.  Gradasi Kompetensi</b></h6>
							<ol>
                <li>Kompetensi tahun 1</li>
									<p>”Menguasai dasar pemograman & logikanya, menguasai dasar sistem informasi”</p>
                <li>Kompetensi tahun 2</li>
									<p>”Mampu membangun program sederhana, mengkoneksikan dengan database”</p>
                <li>Kompetensi tahun 3</li>
									<p>“Mampu membangun sistem informasi”</p>
								<li>Kompetensi tahun 4</li>
									<p>”Mampu mengaplikasikan sistem informasi di dunia nyata”</p>
              </ol>

              <h5><b>1.4.   Kurikulum</b></h5>
              <p>Sesuai dengan Keputusan Menteri Pendidikan Nasional RI No. 232 Tahun 2000 tentang Pedoman Penyusunan Kurikulum Pendidikan Tinggi dan Penilaian Hasil Belajar Mahasiswa, jurusan/program studi Sistem Informasi jenjang Strata 1 (SI-S1), pada tahun akademik 2004/2005 mengikuti pola yang dianjurkan oleh  SK Mendiknas tersebut.</p>
							<p>Setelah melalui proses yang cukup panjang, kurikulum berbasis kompetensi program studi SI diberlakukan bagi semua mahasiswa SI yang belum menyelesaikan studinya hingga tahun akademik 2003/2004 berakhir.</p>
							<p>Program studi ini mengarahkan mahasiswa untuk membangun sistem informasi berbasis komputer dengan dua peminatan bisnis dan akuntansi. Dengan demikian, lulusan diharapkan untuk bisa</p>
              <ol>
                <li>Mempunyai wawasan dan penguasaan tentang pengetahuan dasar komputer, sistem informasi dan penggunaan aplikasi-aplikasi untuk membantu pelaksanaan proses bisnis dan manajemen</li>
                <li>Mempunyai kemampuan dalam rancang bangun sistem informasi berbasis komputer dan implementasinya dengan menggunakan konsep-konsep, metode-metode, teknik dan alat bantu tertentu beserta dokumentasinya</li>
                <li>Mempunyai kemampuan akademik untuk mengembangkan bidang ilmu/ teknologi sesuai tridarma perguruan tinggi</li>
								<li>Mempunyai sikap dan etika profesi yang tinggi serta mampu beradaptasi berdasarkan ketaqwaan terhadap Tuhan Yang Maha Esa dan kecintaan terhadap tanah air</li>
              </ol>

              <h5><b>1.5.   Syarat Kelulusan</b></h5>
              <p>Untuk menyelesaikan pendidikan sarjana pada program studi SI-S1, mahasiswa diwajibkan telah mengumpulkan sekurang-kurangnya 144 sks mata kuliah yang meliputi mata kuliah wajib sebanyak 100 sks (termasuk Tugas Akhir dalam bentuk skripsi atau kerja praktek dan workshop) dan sisanya adalah mata kuliah pilihan minimal sebanyak 44 sks.</p>
							<ol>
								<li>Mata Kuliah Wajib. Mata kuliah wajib merupakan mata kuliah yang wajib diambil oleh mahasiswa. Mata kuliah wajib program studi terdiri atas 100 sks. Bagi mahasiswa yang mengambil peminatan Sistem Informasi Bisnis diwajibkan mengambil mata kuliah wajib minat Sistem Informasi Bisnis (lihat tabel mata kuliah minat), sedangkan mahasiswa yang mengambil peminatan Sistem Informasi Akuntansi dapat mengambil mata kuliah minat Sistem Informasi Akuntansi. Persyaratan mata kuliah wajib sebesar 100 sks ini dapat dipenuhi dengan mengambil semua mata kuliah wajib. Semua mata kuliah wajib dan praktikum nilai minimum adalah C (nilai D berarti tidak lulus).</li>
							</ol>
							<p>[1]   Mata Kuliah Pilihan. Pada dasarnya mahasiswa bebas memilih mata kuliah pilihan yang tersedia dan/atau mengambil mata kuliah wajib minat SIA untuk yang mengambil wajib minat SIB dan sebaliknya. Namun demikian, agar mahasiswa memiliki suatu kesatuan kemampuan yang memadai terutama agar mata kuliah pilihan menunjang Tugas Akhir dan persiapan masuk dunia kerja, maka dalam memilih mata kuliah ini mahasiswa berkonsultasi dan memperhatikan saran-saran dosen pembimbing akademik. Jika mahasiswa ingin mengambil mata kuliah di luar tabel mata kuliah pilihan maka yang bersangkutan harus memberi tahu dan berkonsultasi dengan dosen pembimbing akademik. Karena suatu alasan, mata kuliah pilihan dapat berubah distribusi semesternya dari semester ganjil ke semester genap atau sebaliknya.</p>
							<p>[2]   Skripsi. Tugas akhir mahasiswa dapat berupa skripsi atau kerja praktek dengan bobot 6 sks, dan harus diambil oleh setiap mahasiswa.</p>
							<p>[1]       Tujuan</p>
							<ol>
								<li>Melatih mahasiswa untuk dapat berfikir secara logis dan sistematis</li>
								<li>Melatih kemandirian sekaligus team work dalam menyelesaikan permasalahan</li>
								<li>Melatih kemampuan dalam penggunaan produk-produk/tools dalam bidang  terutama untuk keperluan manajemen, bisnis dan akuntansi.</li>
								<li>Melatih kemampuan untuk analisis dan pengembangan sistem informasi  pada suatu institusi atau organisasi.</li>
								<li>Melatih kemampuan untuk menulis karya ilmiah</li>
								<li>Mempersiapkan mahasiswa untuk hidup  bermasyarakat, melanjutkan studi, atau memasuki dunia kerja.</li>
							</ol>
							<p>[2]       Syarat pengambilan tugas akhir</p>
							<ol>
								<li>Telah menempuh 120 sks dengan IPK ≥ 2.00 yang terbaik</li>
								<li>Telah menempuh mata kuliah penunjang topik tugas akhir</li>
								<li>Mencantumkan skripsi di KRS dan mendaftarkan ke bagian Administrasi Akademik.</li>
							</ol>
							<p>[3]       Pelaksanaan Skripsi</p>
							<p>Pada dasarnya, pelaksanaan tugas akhir mahasiswa dibawah bimbingan dosen pembimbing tugas akhir yang sesuai dengan topik yang diambil. Mahasiswa mengerjakan topik tugas akhir secara mandiri dan secara periodik melaksanakan bimbingan kepada dosen pembimbing tugas akhir 1 maupun 2.</p>
							<p>[8]       Workshop. Pada dasarnya mata kuliah ini digunakan untuk menambah pengetahuan/wawasan mahasiswa terhadap perkembangan teknologi dan pelaksanaannya bisa dalam bentuk keikutsertaan seminar, asistensi, penulis dalam media cetak atau bentuk kegiatan lain yang relevan.</p>

