            <center><h4><b>Teknik Komputer (D3)</b></h4></center>

              <h5><b>1.1.   Pendahuluan</b></h5>
              <p>Dilandasi kesadaran potensi pesatnya kemajuan teknologi elektronika, program studi Teknik Komputer jenjang D3 merupakan bagian Jurusan Teknik Komputer yang mengemban amanat melatih dan menghasilkan ahli madya yang berkecimpung dalam lingkup teknik elektronika, microprocessor, instrumentasi kendali, dan telekomunikasi. Peran ahli madya bidang ini antara lain untuk menjembatani level perancang dan pengguna teknologi, melakukan supervisi proses produksi, melakukan instalasi-instalasi systems dan networking untuk berbagai keperluan mulai dari perkantoran, housing, hotels, medis, sarana navigasi, hingga entertainment support.</p>
              <p>Mahasiswa program ini disarankan menyelesaikan beban akademik 110 sks dan latihan kerja dalam rentang 6 semester. Frame akademik dalam program ini sarat kegiatan praktek dengan peranti hi-tech measurement dan device programming, sehingga mahasiswa dituntut tekun dan berlatih mengembangkan logika. Alur matakuliah dan praktik yang ada mengindikasikan kepada konsentrasi Instrumentasi, Future Control, dan Jaringan Computer. Pada akhir masa studi, mahasiswa dilatih menyelesaikan laporan tugas akhir, yang menunjukkan penguasaan penyelesaian pekerjaan tertentu dalam lingkup yang disebut di muka. Mahasiswa yang mampu dapat melanjutkan studi pada jenjang selanjutnya (D4/S1), bidang yang sesuai umumnya adalah dengan lingkup keteknikan dan systems.</p>

              <h5><b>1.2.   Kompetensi Lulusan</b></h5>
              <h6><b>1.2.1.  Kompetensi Umum</b></h6>
              <ol>
                <li>Mampu mengimplementasikan TI secara kreatif dan inovatif di masyarakat</li>
                <li>Mampu mengembangkan diri untuk peningkatan wawasan dan keahlian</li>
                <li>Mampu beradaptasi dan bekerjasama dalam team work</li>
                <li>Mampu berkomunikasi aktif dalam bahasa internasional</li>
                <li>Mampu menjunjung tinggi etika dan profesionalisme</li>
              </ol>

              <h6><b>1.2.2.  Kompetensi Khusus</b></h6>
              <ol>
                <li>Membangun dan merawat infrastruktur network</li>
                <li>Mampu membangun komputer embedded (piranti-piranti yg tersambung dengan sistem komputer)</li>
                <li>menguasasi tool simulasi elektronik dan network</li>
              </ol>

              <h6><b>1.2.3.  Gradasi Kompetensi</b></h6>
              <ol>
                <li>Tahun ke-1 :</li>
								<ol type="a">
									<li>mampu menginstalasi komputer dan sistem operasi stand alone,</li>
									<li>mampu mengelola peranti-piranti  elektronik.</li>
								</ol>
                <li>Tahun ke-2 :</li>
								<ol type="a">
									<li>menyusun program komputer sederhana,</li>
									<li>melakukan pemeliharaan komputer dan troubleshooting.</li>
								</ol>
                <li>Tahun ke-3 :</li>
								<ol type="a">
									<li>menyelesaikan persoalan bisnis berbasis computer embedded,</li>
									<li>menyelesaikan persoalan bisnis berbasis jaringan komputer enterprise.</li>
								</ol>
              </ol>

							<h5><b>1.3.   Syarat Kelulusan</b></h5>
              <p>Untuk menyelesaikan pendidikan diploma 3 pada program studi Teknik Komputer, mahasiswa diwajibkan untuk mengumpulkan sekurang-kurangnya 110 sks mata kuliah yang meliputi mata kuliah wajib sebanyak 80 sks, dan lainnya merupakan mata kuliah pilihan.</p>
              <ol>
                <li>Mata Kuliah Wajib.   Mata kuliah wajib merupakan mata kuliah yang wajib diambil oleh semua mahasiswa. Mata kuliah wajib program studi terdiri atas 80 sks. Persyaratan mata kuliah wajib sebesar 80 sks ini dapat dipenuhi dengan mengambil semua mata kuliah wajib. Semua mata kuliah wajib dan praktikum nilai minimum adalah C (nilai D berarti tidak lulus).</li>
                <li>Mata Kuliah Pilihan . Mata kuliah pilihan dimaksudkan untuk memberi arah dalam penyelesaian tugas akhir. Terdapat dua mata kuliah pilihan, yaitu mata kuliah Pilihan I dan Pilihan II. Mata kuliah pilihan I berbobot 2 sks adalah mata kuliah lintas jurusan, atau mata kuliah pilihan yang ditawarkan  secara bergantian dari semester ke semester. Mata kuliah Pilihan II terdiri atas 4 sks teori dan 2 sks praktikum, mata kuliah pilihan II menunjukkan arah pendalaman pada bidang jaringan komputer atau mikrokontroler.</li>
                <li>Tugas Akhir. Tugas akhir mempunyai bobot 4 sks, dapat berupa pembuatan suatu sistem yang berbasis mikrokontroler atau mikrokomputer, atau rekayasa perangkat keras dan perangkat lunak yang bertujuan meningkatkan kemampuan sistem berbasis mikrokontroler atau mikrokomputer yang berdiri sendiri atau dalam bentuk jaringan.</li>
              </ol>
							<p><b>[1]       Tujuan pembuatan tugas akhir</b></p>
							<p>Melatih mahasiswa agar dapat merealisasikan suatu sistem berbasis mikrokontroler atau mikrokomputer, yang terdiri atas perangkat keras dan perangkat lunak.</p>
