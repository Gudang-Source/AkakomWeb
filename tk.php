							<p>Mencantumkan tugas akhir dalam KRS dan mendaftarkan pemilihan pembimbing tugas akhir ke bagian Administrasi Akademik.</p>

              <h5><b>1.4.   Pelaksanaan Tugas Akhir</b></h5>
              <p>Pelaksanaan tugas akhir dibawah bimbingan dosen pembimbing tugas akhir yang sesuai dengan bidang permasalahan yang diambil. Mahasiswa dapat memilih tugas akhir berupa rekayasa perangkat keras, kerja praktek, rekayasa piranti praktikum , dan jenis-jenis lain yang direkomendasikan  oleh program studi.</p>

