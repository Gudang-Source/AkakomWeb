            <center><h4><b>Manajemen Informatika ( D3 )</b></h4></center>

              <h5><b>1.1.   Pendahuluan</b></h5>
              <p>Dalam situasi perekonomian saat ini, pemerintah Indonesia memerlukan upaya-upaya baru untuk mengembalikan kehidupan perekonomian yang lebih baik. Salah satu bentuk upaya yang sedang dilakukan adalah sedikit menggeser prioritas perekonomian nasional dari bentuk resource-base economy ke bentuk knowledge-base economy. Dengan pola pergeseran ini, peran IPTEK menjadi sangat penting untuk mendukung visi Pembangunan Jangka Panjang (RPJP) tahun 2005-2025 yaitu “Indonesia Yang Maju, Mandiri, dan Adil”. Sejalan dengan hal tersebut, banyak organisasi yang telah berkembang ke bentuk dynamic network. Dalam dynamic network organization, peranan TI menjadi sangat penting.</p>
							<p>Jurusan/program studi Manajemen Informatika dengan jenjang D3 diharapkan dapat memberikan kontribusi untuk mewujudkan kebutuhan tenaga-tenaga siap pakai pada semua bidang TI. Dalam mewujudkan harapan tersebut maka jurusan/program studi Manajemen Informatika membuat kurikulum yang berbasis kompetensi.</p>
							<p>Sesuai dengan Keputusan Menteri Pendidikan Nasional RI No. 232 Tahun 2000 tentang Pedoman Penyusunan Kurikulum Pendidikan Tinggi dan Penilaian Hasil Belajar Mahasiswa, jurusan/program studi Manajemen Informatika jenjang D3 (MI-D3), pada tahun akademik 2004/2005 mengikuti pola yang dianjurkan oleh  SK Mendiknas tersebut. Setelah melalui proses yang cukup panjang, kurikulum berbasis kompetensi program studi MI diberlakukan bagi semua mahasiswa MI yang belum menyelesaikan studinya hingga tahun akademik 2003/2004. Selain hasil kurikulum yang dituangkan dalam bentuk distribusi semesteran, pengembangan isi dan materi silabi juga diberlakukan.</p>

              <h5><b>1.2.   Kompetensi</b></h5>
              <h6><b>1.2.1.  Kompetensi Umum</b></h6>
              <ol>
                <li>Mampu mengimplementasikan TI secara kreatif dan inovatif di masyarakat</li>
                <li>Mampu mengembangkan diri untuk peningkatan wawasan dan keahlian</li>
                <li>Mampu beradaptasi dan bekerjasama dalam team work</li>
                <li>Mampu berkomunikasi aktif dalam bahasa internasional</li>
                <li>Mampu menjunjung tinggi etika dan profesionalisme</li>
              </ol>

              <h6><b>1.2.2.  Kompetensi Khusus</b></h6>
              <ol>
                <li>Trampil mengimplemetasikan  rancangan sistem informasi bisnis berbasis desktop dan web</li>
                <li>Mampu membangun animasi multimedia</li>
                <li>Menguasai tools multimedia dan web</li>
              </ol>

              <h6><b>1.2.3.  Gradasi Kompetensi</b></h6>
              <ol>
                <li>Tahun ke-1 :</li>
									<p>“Memahami dasar-dasar sistem informasi dan mampu melakukan operasi basis data”</p>
                <li>Tahun ke-2 :</li>
									<p>“Mampu  membangun aplikasi basis data  berbasis desktop dan web ”</p>
                <li>Tahun ke-3 :</li>
									<p>“Mampu membangun aplikasi berbasis web dinamis”</p>
              </ol>

							<h6><b>1.2.4.  Usaha yang dilakukan</b></h6>
							<ol>
                <li>Peningkatan kualitas dosen secara terus menerusb</li>
                <li>Memberikan materi workshop lebih dinamis</li>
                <li>Evaluasi kurikulum dilakukan secara periodik</li>
								<li>TOEFL menjadi syarat kelulusan</li>
                <li>Memfasilitasi kegiatan kelompok mahasiswa jurusan</li>
								<li>Memfasilitasi kegiatan kelompok dosen</li>
                <li>Proses belajar mengajar ditingkatkan secara terus menerus</li>
              </ol>

							<h5><b>1.3.   Syarat Kelulusan</b></h5>
              <p>Untuk menyelesaikan pendidikan diploma pada program studi MI-D3, mahasiswa diwajibkan telah menyelesaikan sekurang-kurangnya 110 sks mata kuliah yang meliputi mata kuliah wajib sebanyak 80  sks  dan sisanya adalah mata kuliah pilihan.</p>
              <ol>
                <li>Mata Kuliah Wajib.</li>
                <p>Mata kuliah wajib merupakan mata kuliah yang harus diambil oleh mahasiswa yang terdiri atas wajib nasional, wajib STMIK AKAKOM dan wajib program studi. Persyaratan mata kuliah wajib sebesar 80 sks ini dapat dipenuhi dengan mengambil semua mata kuliah wajib (status W pada kurikulum). Semua mata kuliah wajib dan praktikum nilai minimum adalah C (nilai D berarti tidak lulus).</p>
                <li>Mata Kuliah Pilihan.</li>
                <p>Pada dasarnya mahasiswa bebas memilih mata kuliah pilihan yang tersedia. Namun demikian, agar mahasiswa memiliki suatu kesatuan kemampuan yang memadai, maka dalam memilih mata kuliah ini mahasiswa berkonsultasi dan memperhatikan saran-saran dosen pembimbing akademik. Jika mahasiswa ingin mengambil mata kuliah di luar tabel mata kuliah pilihan maka yang bersangkutan harus memberi tahu dan berkonsultasi dengan dosen pembimbing akademik. Karena suatu alasan, mata kuliah pilihan dapat berubah distribusi semesternya dari semester ganjil ke semester genap atau sebaliknya.</p>
                <li>Tugas Akhir.</li>
                <p>Tugas akhir mahasiswa dapat berupa penugasan atau kerja praktek dengan bobot 4 sks, dan harus diambil oleh setiap mahasiswa.</p>
                <ol type="a">
                  <li>Tujuan</li>
                  <ol>
                    <li>Melatih mahasiswa untuk dapat berfikir secara logis dan sistematis</li>
                    <li>Melatih kemandirian sekaligus team work dalam menyelesaikan permasalahan</li>
                    <li>Melatih kemampuan dalam penggunaan produk-produk / tools dalam bidang Teknologi Informasi</li>
                    <li>Melatih kemampuan untuk menulis karya ilmiah</li>
                    <li>Mempersiapkan mahasiswa untuk hidup  bermasyarakat, memasuki dunia kerja, atau melanjutkan studi.</li>
                  </ol>
                  <li>Syarat pengambilan tugas akhir</li>
                  <p>Mahasiswa yang akan mengambil tugas akhir disyaratkan sebagai berikut :</p>
                  <ol>
                    <li>Telah menempuh minimal 90 sks dengan IPK ≥ 2.00 yang terbaik</li>
                    <li>Mencantumkan tugas akhir di KRS dan mendaftarkan ke bagian Administrasi Akademik.</li>
                  </ol>
                  <li>Pelaksanaan Tugas Akhir</li>
                  <p>Pada dasarnya, pelaksanaan tugas akhir mahasiswa dibawah bimbingan dosen pembimbing tugas akhir yang sesuai dengan topik yang diambil tugas akhirnya. Mahasiswa memilih sendiri dosen pembimbing tugas akhirnya, saat mendaftarkan diri di bagian Administrasi Akademik.</p>
                  <p>Tugas akhir  di program studi Manajemen Informatika memiliki 2 pola</p>
                  <p>[1]       Mandiri</p>
                  <p>Tugas Akhir mandiri adalah pelaksanaan tugas akhir yang dilakukan secara konvensional yang selama ini sudah dilakukan yakni mahasiswa mengerjakan tugas akhir tersebut secara mandiri dibawah arahan seorang pembimbing.</p>
                  <p>Syarat Tugas akhir pola ini adalah</p>
                  <ol>
                    <li>IPK minimal 2,75, atau</li>
                    <li>Mendapat rekomendasi dari seorang dosen, bahwa mahasiswa yang bersangkutan diprediksi mampu melaksanakan tugas akhir secara mendiri  (untuk   IPK<2,75)</li>
                  </ol>
                  <p>[2]       Laboratory</p>
                  <p>Pelaksanaan tugas akhir pola ini, mahasiswa di kelompokkan dalam 1 kelas, dan penyusunan tugas akhir pelaksanaannya terjadwal  dengan pembimbing adalah tim.</p>
                  <p>Syarat Tugas akhir pola ini adalah</p>
                  <ol>
                    <li>IPK <2,75, atau</li>
                    <li>Mengajukan secara khusus untuk mengikuti pola ini bagi mahasiswa dengan IPK>=2,75</li>
                  </ol>
                </ol>
              </ol>

              <h5><b>1.4.   Peluang</b></h5>
              <p>Lulusan dari jurusan/program studi Manajemen Informatika masih banyak dibutuhkan oleh stakeholder, hal ini dapat dilihat dari banyaknya permintaan lulusan D3 yang ditempatkan pada bagian programmer, teknisi dan software maintenance.  Masih banyaknya peluang lulusan D3 Manajemen Informatika untuk tenaga di bidang TI, memberikan kesempatan yang sangat terbuka agar jurusan dapat mengembangkan diri menjadi lebih adaptif terhadap kebutuhan pasar tenaga kerja bidang TI.</p>
              <p>Kemampuan yang lebih dari setiap lulusan akan memberikan daya saing yang baik untuk peluang di dunia kerja.</p>

