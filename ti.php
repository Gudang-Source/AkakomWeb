            <center><h4><b>Teknik Informatika ( S1 )</b></h4></center>

              <h5><b>1.1.   Pendahuluan</b></h5>
              <p>Dalam situasi perekonomian saat ini, pemerintah Indonesia memerlukan upaya-upaya baru untuk mengembalikan kehidupan perekonomian yang lebih baik. Salah satu bentuk upaya yang sedang dilakukan adalah sedikit menggeser prioritas perekonomian nasional dari bentuk resource-base economy ke bentuk knowledge-base economy. Dengan pola pergeseran ini, peran IPTEK menjadi sangat penting untuk mendukung visi Pembangunan Jangka Panjang (RPJP) tahun 2005-2025 yaitu “Indonesia Yang Maju, Mandiri, dan Adil”. Sejalan dengan hal tersebut, banyak organisasi yang telah berkembang ke bentuk dynamic network. Dalam dynamic network organization, peranan Teknologi Informasi menjadi sangat penting.</p>
              <p>Bidang teknologi informasi, telah merambah secara luas dalam berbagai bidang kehidupan seperti manajemen, teknik, kedokteran, psikologi, pendidikan hingga seni. Dari kombinasi tersebut telah dihasilkan produk-produk seperti otomatisasi sistem, desain produk, pembuatan musik, aplikasi mobile, yang semuanya telah memberikan kontribusi yang besar bagi kehidupan manusia.</p>
							<p>Berdasarkan paparan diatas, diisyaratkan makin  dibutuhkannya sumber daya manusia bidang teknologi informasi. Selain itu juga menandai bahwa bidang teknologi informasi akan terus berkembang sesuai dengan perkembangan ilmu pengetahuan dan teknologi pada umumnya.Program Studi Teknik Informatika (PSTI) merupakan salah satu cabang dari pohon ilmu Informatika dan Komputer, yang turut mengembangkan bidang ilmu teknologi informasi. Adapun penekanannya pada aspek penyimpanan, pengolahan, dan penyampaian informasi secara algoritmik dengan bantuan komputer. Tiga (3) paradigma yang digunakan adalah: paradigma teori, paradigma abstraksi, dan paradigma perancangan. Ketiga paradigma ini saling melengkapi dan kait-mengait antara satu dengan yang lain. Secara umum ketiga paradigma tersebut (teori, abstraksi, perancangan) merupakan prinsip dasar dari masing-masing bidang ilmu yang diwakili matematika, ilmu alam, teknik).</p>
              <p>Program Studi Teknik Informatika (PSTI) merupakan salah satu cabang dari pohon ilmu Informatika dan Komputer, yang turut mengembangkan bidang ilmu teknologi informasi. Adapun penekanannya pada aspek penyimpanan, pengolahan, dan penyampaian informasi secara algoritmik dengan bantuan komputer. Tiga (3) paradigma yang digunakan adalah: paradigma teori, paradigma abstraksi, dan paradigma perancangan. Ketiga paradigma ini saling melengkapi dan kait-mengait antara satu dengan yang lain. Secara umum ketiga paradigma tersebut (teori, abstraksi, perancangan) merupakan prinsip dasar dari masing-masing bidang ilmu yang diwakili matematika, ilmu alam, teknik).</p>

              <h5><b>1.2.   Sekilas Program Studi Teknik Informatika</b></h5>
              <p>Program Studi Teknik Informatika mulai diselenggarakan berdasarkan SK No. 300/DIKTI/Kep/1992. Saat ini Program Studi Teknik Informatika telah diakreditasi ‘B’ berdasarkan SK No. 028/BAN-PT/Ak-IX/S1/I/2006.</p>
              <p>Minat untuk mengikuti pendidikan pada Program Studi Teknik Informatika sangat besar dan meningkat dari tahun ke tahun dan berasal dari semua propinsi di Indonesia. Hal ini dikarenakan kebutuhan alumni Program Studi Teknik Informatika   cukup tinggi karena hampir semua aspek membutuhkan teknologi informasi untuk berkembang.  Selain itu juga mengisyaratkan bahwa Program Studi Teknik Informatika  sudah dikenal dalam taraf nasional.</p>
							<p>Jumlah alumni Program Studi Teknik Informatika  hingga saat ini mencapai sekitar 1000 mahasiswa, yang telah bekerja pada perbankan, perminyakan, pendidikan, kesehatan , dan bidang-bidang lain. Banyak mahasiswa yang telah bekerja sebelum lulus dari pendidikannya. Hal ini membuktikan bahwa ilmu yang diterima mahasiwa bisa digunakan kapan saja. Beberapa lapangan kerja alumni diantaranya Krakatau Steel, BRI, Pertamina, TOTAL, (SGM), Instansi Negeri (Kejaksaaan, Pemda, PTN), dan Perguruan Tinggi Swasta.</p>
              <p>Kegiatan-kegiatan yang dilaksanakan oleh Program Studi Teknik Informatika  sangat beragam. Kegiatan tersebut dalam bentuk penelitian bersama dosen dan mahasiswa, partisipan / penyelenggara even  nasional maupun internasional  (seminar, workshop, kuliah umum), pelatihan teknologi informasi ke instansi/organisasi.</p>

              <h5><b>1.3 Visi, Misi, Tujuan, dan Sasaran serta Strategi Pencapaian</b></h5>
              <h6><b>1.3.1      Jelaskan mekanisme penyusunan visi, misi, tujuan dan sasaran program studi, serta pihak-pihak yang dilibatkan.</b></h6>
							<p>Dalam penyusunan Visi, Misi dan tujuan, Program Studi Teknik Informatika berpedoman kepada visi dan misi dari Institusi. Program studi mengadakan rapat pembahasan untuk menghasilkan draft visi, misi dan tujuan program studi berdasarkan visi, misi dan tujuan institusi.</p>
							<ul>
                <li>Hasil rapat disosialisasikan dalam bentuk edaran  ke seluruh civitas akademika, untuk mendapatkan masukan terhadap visi, misi dan tujuan program studi.</li>
                <li>Hasil dari masukan terhadap draft visi, misi dan tujuan dibahas kembali dalam rapat pleno untuk memutuskan visi, misi dan tujuan final program studi.</li>
                <li>Sosialisasi hasil akhir visi, misi dan tujuan program studi dalam bentuk   website program studi, dosen jurusan dan alumni, serta pertemuan dengan civitas akademika.</li>
              </ul>

              <h6><b>1.3.2  Visi</b></h6>
              <p>Jurusan Teknik Informatika sebagai pembentukan dan pengembangan sumber daya manusia yang mempunyai wawasan ICT (Information and Communication Technology) yang tinggi dalam bidang Rekayasa Perangkat Lunak (Mobile, Games & Jaringan) dan Sistem Cerdas/Komputasi</p>

              <h6><b>1.1.3  Misi</b></h6>
              <p>Mendidik Mahasiswa untuk siap produktif dan mampu mengembangan diri di dalam penguasaan ICT (Information and Communication Technology) khususnya pada bidang Rekayasa Perangkat Lunak (Mobile, Games & Jaringan) maupun bidang Sistem Cerdas/Komputasi, serta menerapakan dalam kehidupan bermasyarakat</p>

							<h6><b>1.3.4  Tujuan</b></h6>
              <p>Menghasilkan lulusan yang siap bekerja / produktif dalam dunia kerja baik secara tim maupun individu dalam penguasaan ICT (Information and Communication Technology) khususnya pada bidang Rekayasa Perangkat Lunak (Mobile, Games & Jaringan)) maupun bidang Sistem Cerdas/Komputasi dan dapat bekerjasama dalam tingkat lokal, nasional, regional maupun internasional</p>

							<h5><b>1.4.   Kompetensi</b></h5>
							<h6><b>1.4.1.  Kompentesi Umum</b></h6>
              <ol>
                <li>Mampu mengimplementasikan TI secara kreatif dan inovatif di masyarakat</li>
                <li>Mampu mengembangkan diri untuk peningkatan wawasan dan keahlian</li>
                <li>Mampu beradaptasi dan bekerjasama dalam team work</li>
                <li>Mampu berkomunikasi aktif dalam bahasa internasional</li>
                <li>Mampu menjunjung tinggi etika dan profesionalisme</li>
              </ol>

              <h6><b>1.4.2.  Kompentensi Khusus</b></h6>
              <p>Mampu menganalisis, merancang, dan membangun perangkat lunak berbasis sistem Cerdas, Mobile, Games, Jaringan menggunakan teknik dan tools, beserta pendokumentasiannya</p>

              <h6><b>1.4.3.  Gradasi Kompetensi</b></h6>
							<ol>
                <li>Kompetensi tahun 1</li>
									<p>“Menguasai dasar pemrograman, Logika pemrograman & berpikir analitis”</p>
                <li>Kompetensi tahun 2</li>
									<p>“Memahami Metode Pengembangan Perangkat Lunak”</p>
                <li>Kompetensi tahun 3</li>
									<p>“Mampu Merancang Sistem Perangkat Lunak Sederhana”</p>
								<li>Kompetensi tahun 4</li>
									<p>“Mampu Mengimplementasikan Perangkat Lunak” (Cerdas, Mobile, Games dan Jaringan)</p>
              </ol>

							<h6><b>1.4.4.  Usaha</b></h6>
							<p>Untuk mencapai kompetensi di atas, Program Studi Teknik Informatika  didukung oleh:</p>
							<ol>
								<li>Tenaga pengajar 80% S2 dan S3 dan berkompeten dibidang teknologi informasi.</li>
								<li>Kelas laboratorum (KL) yang dikembangkan menggunakan pola pengajaran interaktif. Dalam kelas ini  mahasiswa akan mempraktekkan secara langsung teori atau konsep yang diajarkan, dengan bimbingan dosen pengampu dan menggunakan sarana yang sangat memadai. Pada tahun ini akan dikembangkan KL Administrasi dan Jaringan Komputer, Database dengan ORACLE, Mircosoft  .Net ;</li>
								<li>Workshop, sebagai salah satu mata kuliah dalam kurikulum yang memberikan menambah pengetahuan/wawasan mahasiswa terhadap perkembangan teknologi informasi.</li>
								<li>Character Building, merupakan sarana pembentukan karakter mahasiswa agar menjadi lulusan yang bertakwa, inovatif, dan mandiri.</li>
								<li>Study Club, kelompok studi yang digunakan sebagai sarana untuk mengembangkan bakat dan hobi mahasiswa dalam bidang teknologi informasi.</li>
								<li>Pelatihan Bahasa Inggris (TOEFL)</li>
							</ol>

              <h5><b>1.5. Kurikulum</b></h5>
							<p>Kurikulum podi Teknik Informatika disusun mengacu stantar kurikulum internasional ”Curricula 2005”  yang dipadukan dengan kebutuhan teknologi informasi baik dalam skala nasional maupun internasional  yang ditekankan pada real problem solution.</p>
							<p>Silabi pada dasarnya ditinjau setiap tahun oleh pakar-pakar dalam bidang tertentu. Pengembangan perlu dilakukan untuk mengisi dan mengevaluasi materi-materi teknologi informasi yang sangat dinamis. Sedangkan peninjauan kurikulum dilaksanakan sekitar  4-5 tahun.</p>

							<h5><b>1.6.   Syarat Kelulusan</b></h5>
							<p>Untuk menyelesaikan pendidikan sarjana pada Program Studi Teknik Informatika, mahasiswa diwajibkan telah mengumpulkan minimal 144 sks mata kuliah yang meliputi mata kuliah wajib sebanyak 105 sks (termasuk Tugas Akhir dalam bentuk skripsi atau kerja praktek dan workshop) dan sisanya adalah mata kuliah pilihan minimal 39 sks.</p>
							<p>Mata Kuliah Wajib</p>
							<p>Mata kuliah wajib merupakan mata kuliah yang wajib diambil oleh mahasiswa. Mata kuliah wajib terdiri atas mata kuliah wajib Institusi atau wajib STMIK (tanda AK) dan wajib program studi  dengan diberi tanda W pada status yang ada di kurikulum program studi Teknik Informatika. Persyaratan mata kuliah wajib sebesar 105 sks ini dapat dipenuhi dengan mengambil semua mata kuliah wajib institusi dan mata kuliah wajib STMIK  dan selebihnya diambil dari mata kuliah wajib program studi.  Semua mata kuliah wajib dan praktikum nilai minimum adalah C (nilai D berarti tidak lulus).</p>
							<p>Mata Kuliah Pilihan</p>
							<p>Pada dasarnya mahasiswa bebas memilih mata kuliah pilihan yang tersedia (lihat tabel mata kuliah pilihan). Namun demikian, agar mahasiswa memiliki suatu kesatuan kemampuan yang memadai, maka dalam memilih mata kuliah ini mahasiswa berkonsultasi dan memperhatikan saran-saran dosen pembimbing akademik. Jika mahasiswa ingin mengambil mata kuliah di luar tabel mata kuliah pilihan maka yang bersangkutan harus memberi tahu dan berkonsultasi dengan dosen pembimbing akademik.      Karena suatu alasan, mata kuliah pilihan dapat berubah distribusi semesternya dari semester ganjil ke semester genap atau sebaliknya.</p>

							<h5><b>1.7.   Skripsi</b></h5>
							<p>Skripsi merupakan salah tugas akhir dari mahasiswa Strata-1 yang merupakan mata kuliah wajib yang harus di ambil.</p>
							<p>Tujuan</p>
							<ol>
								<li>Melatih mahasiswa untuk dapat berfikir secara logis dan sistematis</li>
								<li>Melatih kemandirian sekaligus team work dalam menyelesaikan permasalahan</li>
								<li>Melatih kemampuan dalam penggunaan produk-produk / tools dalam bidang Teknologi Informasi</li>
								<li>Melatih kemampuan untuk menulis karya ilmiah</li>
								<li>Mempersiapkan mahasiswa untuk hidup  bermasyarakat, melanjutkan studi, atau memasuki dunia kerja.</li>
							</ol>
							<p>Syarat pengambilan skripsi</p>
							<ol>
								<li>Telah menempuh 120 sks dengan IPK ≥ 2.00 yang terbaik</li>
								<li>Telah menempuh mata kuliah penunjang topik tugas akhir</li>
								<li>Mencantumkan tugas akhir di KRS dan mendaftarkan ke bagian Administrasi Akademik.</li>
							</ol>
							<p>Pelaksanaan Skripsi</p>
							<p>Pada dasarnya, pelaksanaan skripsi mahasiswa dibawah bimbingan dosen pembimbing tugas akhir yang sesuai dengan topik yang diambil tugas akhirnya. Mahasiswa memilih sendiri dosen pembimbing tugas akhirnya, saat mendaftarkan diri di bagian Administrasi Akademik.</p>
							<p>Mahasiswa wajib membuat proposal skripsi dibawah bimbingan dosen pembimbing, jika dosen pembimbing telah menyetujui, proposal skripsi di kumpulkan pada bagian Administrasi Akademik dan akan dibuatkan jadwal seminar dengan diuji oleh 2 orang dosen sebagai narasumber (narasumber tersebut juga yang akan menguji pada saat ujian pendadaran), jika proposal tersebut LOLOS maka dapat diteruskan sebagai skripsi dan jika TIDAK LOLOS mahasiswa tersebut diwajibkan mengulang dengan telah memperbaiki proposal tersebut.</p>

